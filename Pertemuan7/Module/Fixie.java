class Fixie {
    public String merk; 
    public String stang;
    public String jok;
    public String gear;
    public String ban;

    public String getMerk() {
        return merk;
    }

    public void setMerk(String newMerk) {
        this.merk = newMerk;
    }
    
    public String getStang() {
        return stang;
    }

    public void setStang(String newStang) {
        this.stang = newStang;
    }

    public String getJok() {
        return jok;
    }
        
    public void setJok(String newJok) {
        this.jok = newJok;
    }
    
    public String getGear() {
        return gear;
    }

    public void setGear(String newGear) {
        this.gear = newGear;
    }

    public String getBan() {
        return ban;       
    }

    public void setBan(String newBan) {
        this.ban = newBan;
    }   
}